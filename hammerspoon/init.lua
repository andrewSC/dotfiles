-- Global options
hs.window.animationDuration = 0

-- Fullscreen
hs.hotkey.bind({"alt", "cmd"}, "X", function()
  local win = hs.window.focusedWindow()
  local f = win:frame()
  local screen = win:screen()
  local max = screen:frame()

  f.x = max.x
  f.y = max.y
  f.w = max.w
  f.h = max.h
  win:setFrame(f)
end)

